class RandomDouble{
	
	      public static void main(String[] args) {
      	
			       double a = (double)(Math.random() * 20);  //(int)(Math.random() * 20)
    	
			       System.out.println(a);

			       double b = 10 + (double)(Math.random() * 10); //10 + (int)(Math.random() * 10)

			       System.out.println(b);

			       double c = 10 + (double)(Math.random() * 41);  //10 + (int)(Math.random() * 41) 

			       System.out.println(c);

			       double d = (double)(Math.random() * 2);   //(int)(Math.random() * 2)
			       
			       System.out.println(d);
  			       
      }


}