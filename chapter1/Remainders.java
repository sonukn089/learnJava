class Remainders{
	

      public static void main(String[] args) {
      	
          System.out.println( 56 % 6 );  // 2

          System.out.println( 78 % -4 );  // 2

          System.out.println( -34 % 5 );  // -4

          System.out.println( -34 % -5 );  // -4

          System.out.println( 5 % 1 );  // 0

          System.out.println( 1 % 5 );  // 1



        }


}